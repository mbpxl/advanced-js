// Карта для поваров и их специализаций
const chefs = new Map([
  ["Пицца 'Маргарита'", "Виктор"],
  ["Пицца 'Пепперони'", "Виктор"],
  ["Суши 'Филадельфия'", "Ольга"],
  ["Суши 'Калифорния'", "Ольга"],
  ["Тирамису", "Дмитрий"],
  ["Чизкейк", "Дмитрий"]
]);

// Объекты клиентов
const alexey = { name: "Алексей" };
const maria = { name: "Мария" };
const irina = { name: "Ирина" };

// Карта для заказов клиентов
const orders = new Map([
  [alexey, ["Пицца 'Пепперони'", "Тирамису"]],
  [maria, ["Суши 'Калифорния'", "Пицца 'Маргарита'"]],
  [irina, ["Чизкейк"]]
]);

// Функция для отображения поваров и заказов
function displayOrders() {
  orders.forEach((dishes, client) => {
    console.log(`Клиент ${client.name} заказал:`);
    dishes.forEach(dish => {
      console.log(`- ${dish} (Повар: ${chefs.get(dish)})`);
    });
  });
}

// Выводим заказы на консоль
displayOrders();
